public class DatiPersona {

    String nome;
    String cognome;
    String sesso;
    String dataNascita;
    String comuneNascita;

    public void setNome(String setNome) {
        this.nome = setNome;
    }

    public void setCognome(String setCognome) {
        this.cognome = setCognome;
    }

    public void setSesso(String setSesso) {
        this.sesso = setSesso;
    }

    public void setDataNascita(String setDataNascita) {
        this.dataNascita = setDataNascita;
    }

    public void setComuneNascita(String setComuneNascita) {
        this.comuneNascita = setComuneNascita;
    }

    public String getNome() {
        return this.nome;
    }

    public String getCognome() {
        return this.cognome;
    }

    public String getSesso() {
        return this.sesso;
    }

    public String getDataNascita() {
        return this.dataNascita;
    }

    public String getComuneNascita() {
        return this.comuneNascita;
    }


}
